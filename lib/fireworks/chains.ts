/** 로고를 그린 뒤 채워진 픽셀에서 count개의 [-1,1] 좌표를 뽑는다 (y는 위가 +).
 *  samplePixelColors가 켜진 체인은 각 좌표의 실제 픽셀 색도 함께 반환한다. */
export function sampleChainPoints(chain: ChainDef, count: number): ChainSample {
  const size = 220;
  const canvas = document.createElement("canvas");
  canvas.width = size;
  canvas.height = size;
  const ctx = canvas.getContext("2d")!;
  ctx.fillStyle = "#fff";
  ctx.strokeStyle = "#fff";
  chain.draw(ctx, size);

  const data = ctx.getImageData(0, 0, size, size).data;
  const candidates: number[] = [];
  for (let y = 0; y < size; y += 2) {
    for (let x = 0; x < size; x += 2) {
      const idx = (y * size + x) * 4;
      if (data[idx + 3] <= 100) continue;
      if (chain.samplePixelColors) {
        // additive 블렌딩에서는 어두운 픽셀이 안 보이므로 (눈·입 등) 구멍으로 남긴다
        const lum = data[idx] * 0.35 + data[idx + 1] * 0.5 + data[idx + 2] * 0.15;
        if (lum < 45) continue;
      }
      candidates.push(x, y);
    }
  }

  const points = new Float32Array(count * 2);
  const colors = chain.samplePixelColors ? new Float32Array(count * 3) : null;
  const total = candidates.length / 2;
  for (let i = 0; i < count; i++) {
    const pick = Math.floor(Math.random() * total);
    const px = candidates[pick * 2];
    const py = candidates[pick * 2 + 1];
    points[i * 2] = (px / size) * 2 - 1;
    points[i * 2 + 1] = 1 - (py / size) * 2;
    if (colors) {
      const idx = (py * size + px) * 4;
      // additive 글로우에서 원색이 살도록 약간 밝힌다
      colors[i * 3] = Math.min(1, (data[idx] / 255) * 1.2 + 0.04);
      colors[i * 3 + 1] = Math.min(1, (data[idx + 1] / 255) * 1.2 + 0.04);
      colors[i * 3 + 2] = Math.min(1, (data[idx + 2] / 255) * 1.2 + 0.04);
    }
  }
  return { points, colors };
}
